#declaração de coleçoes
# Listas
# Listas são coleções ordenadas e mutáveis de elementos.
# Elas podem conter elementos de diferentes tipos e permitem duplicatas.
# Exemplo de lista
lista = ["Fulano", "Ciclano" , "Beltrano"]
tupla = ("Segunda", "Terça" , "Quarta")
dicionario = {"nome": "Fulano", "idade": 25, "email": "fulano@gmail.com"}


#exibindo os tipos de coleçoes
print(f"{lista}: (type(lista)") # <class 'list'>
print(f"{tupla}: (type(tupla)") # <class 'tuple'>    
print(f"{dicionario}: (type(dicionario)") # <class 'dict'>
